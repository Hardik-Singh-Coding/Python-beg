# Single value multiple variables
x = y = z = "Hello World"
print(x)
print(y)
print(z)


# Multiple values multiple variables
q,r,s = "Hindi", "English", "German"
print(q)
print(r)
print(s)


# Unpacking a collection (iterable objects)
fruits = ["Orange", "Peach", "Cherry", "Mango"]
a,b,c,d = fruits
print(a)
print(b)
print(c)
print(d)