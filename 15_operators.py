# Floor division divides and rounds off to the nearest integer
a = 5
b = 2
print(a // b)


# Bitwise opeartors 
x = 5
print(+x)
print(-x)
print(~x)


# Identity operators
p = 20
q = 20
print(p is q)
print(p is not q)


# Membership operator
element1 = "Summer"
part = "mmer"
print(part in element1)
print(part not in element1)



# If two or more ***binary opearators*** have same precedence then the expression is evaluated from left to right. 
# If two or more ***assignment and/or exponentiation operator*** have the same precedence then the expression is evaluated from right to left. 
