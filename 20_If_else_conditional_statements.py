# short-hand if 
if 30>20: print("30 is greater than 20")



# short had if-else ( ternary operator )
print("20 is greater than 30") if 20>30 else print("30 is greater than 20")



# Multiple else statements on the same line
print("20 is greater than 30") if 20>=30 else print("30 and 30 are equal") if 30==30 else print("30 is greater than 20")